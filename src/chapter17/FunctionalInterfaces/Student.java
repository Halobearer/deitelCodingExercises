package chapter17.FunctionalInterfaces;

public class Student {

    private String name;
    private int age;


    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public String toString() {
        return "Student{" +
                "name='" + name + '\n' +
                ", age=" + age + "\n" +
                '}';
    }
}
